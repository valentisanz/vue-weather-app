<template>
  <div id="app" :class="typeof weather.main!='undefined' &&weather.main.temp>16
    ?'warm':''">
    <main>
      <div class="search-box">
        <input
          type="text"
          class="search-bar"
          placeholder="Search..."
          v-model="query"
          @keypress="fetchWeather"
          autofocus
        />
      </div>
      <div class="weather-wrap" v-if="typeof weather.main!='undefined'">
        <div class="location-box">
          <div class="location">{{weather.name}}, {{weather.sys.country}}</div>
          <div class="date">{{dateBuilder()}}</div>
        </div>
        <div class="weather-box">
          <div class="temp">{{Math.round(weather.main.temp)}}ÂºC</div>
          <div class="weather">{{weather.weather[0].main}}</div>
        </div>
      </div>
      <div class="weather-wrap" v-if="typeof weather.main=='undefined'">
        <div class="location-box">
          <div id="wrong" class="location">{{msg}}</div>
        </div>
      </div>
    </main>
  </div>
</template>

<script>
export default {
  name: "App",
  data() {
    return {
      api_key: "8b0b9b2716d867ce3ab05ced1d30b1e9",
      url_base: "https://api.openweathermap.org/data/2.5/",
      query: "",
      weather: {},
      msg: "Enter a location to see the weather",
    };
  },
  methods: {
    async fetchWeather(e) {
      if (e.key == "Enter") {
        const res = await fetch(
          `${this.url_base}weather?q=${this.query}&units=metric&APPID=${this.api_key}`
        );
        this.weather = await res.json();
        if (res.status != 200) {
          this.msg = "Enter a correct location";
        }
      }
    },
    dateBuilder() {
      const today = new Date();
      let dd = today.getDate();

      let mm = today.getMonth() + 1;
      const yyyy = today.getFullYear();
      if (dd < 10) {
        dd = `0${dd}`;
      }

      if (mm < 10) {
        mm = `0${mm}`;
      }
      return `${dd} / ${mm} / ${yyyy}`;
    },
  },
};
</script>

